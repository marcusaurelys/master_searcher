#!/usr/bin/env python3
"""Generate slide-ready figures directly from raw benchmark CSV files.

This replaces symlink/crop behavior and ensures every plot is generated by
Python specifically for the Beamer deck.
"""

from __future__ import annotations

import argparse
from pathlib import Path

try:
    import matplotlib.pyplot as plt
    import numpy as np
    import pandas as pd
except ImportError as exc:
    raise SystemExit(
        "Missing plotting dependencies. Install: pandas matplotlib numpy"
    ) from exc


ALGO_ORDER = [
    "Naive",
    "Rabin-Karp",
    "Vectorized RK",
    "Greedy KMP",
    "Greedy BM",
    "Bird-KMP",
    "Bird-BM",
]

ALGO_COLORS = {
    "Naive": "#e74c3c",
    "Rabin-Karp": "#3498db",
    "Vectorized RK": "#2ecc71",
    "Greedy KMP": "#9b59b6",
    "Greedy BM": "#f39c12",
    "Bird-KMP": "#1abc9c",
    "Bird-BM": "#e67e22",
}


def configure_style() -> None:
    plt.rcParams.update(
        {
            "font.size": 10,
            "axes.titlesize": 11,
            "axes.labelsize": 10,
            "legend.fontsize": 8,
            "xtick.labelsize": 9,
            "ytick.labelsize": 9,
            "figure.facecolor": "white",
            "axes.facecolor": "white",
            "axes.grid": True,
            "grid.alpha": 0.30,
            "grid.linestyle": "--",
            "grid.linewidth": 0.6,
        }
    )


def algo_list_from_frame(df: pd.DataFrame) -> list[str]:
    present = set(df["algorithm"].dropna().unique())
    ordered = [a for a in ALGO_ORDER if a in present]
    extras = sorted(a for a in present if a not in ALGO_ORDER)
    return ordered + extras


def color_for(algo: str) -> str:
    return ALGO_COLORS.get(algo, "#4E5D6C")


def read_csv(path: Path) -> pd.DataFrame:
    if not path.exists():
        raise FileNotFoundError(f"Missing CSV: {path}")
    return pd.read_csv(path)


def save_figure(fig: plt.Figure, *paths: Path) -> None:
    fig.tight_layout()
    for path in paths:
        path.parent.mkdir(parents=True, exist_ok=True)
        if path.is_symlink() or path.exists():
            path.unlink()
        fig.savefig(path, dpi=220, bbox_inches="tight", facecolor="white")
    plt.close(fig)


def cleanup_legacy_files(out_dir: Path) -> None:
    for pattern in ("*_crop_left.png", "*_crop_right.png"):
        for path in out_dir.glob(pattern):
            path.unlink()


def make_subplot_grid(count: int, max_cols: int = 2, panel_w: float = 5.8, panel_h: float = 3.2) -> tuple[plt.Figure, np.ndarray]:
    ncols = max(1, min(max_cols, count))
    nrows = int(np.ceil(count / ncols))
    fig, axes = plt.subplots(nrows, ncols, figsize=(panel_w * ncols, panel_h * nrows), sharey=False)
    if isinstance(axes, np.ndarray):
        flat_axes = axes.reshape(-1)
    else:
        flat_axes = np.array([axes])
    return fig, flat_axes


def plot_random_comparisons_vs_textsize(df_random: pd.DataFrame, out_dir: Path) -> None:
    agg = (
        df_random.groupby(["algorithm", "pattern_size", "text_size"])["comparisons"]
        .mean()
        .reset_index()
    )
    pats = sorted(agg["pattern_size"].unique())
    algos = algo_list_from_frame(agg)

    fig, axes = make_subplot_grid(len(pats), max_cols=2, panel_w=5.6, panel_h=3.2)

    for idx, (ax, pat) in enumerate(zip(axes, pats)):
        sub = agg[agg["pattern_size"] == pat]
        for algo in algos:
            d = sub[sub["algorithm"] == algo].sort_values("text_size")
            if d.empty:
                continue
            ax.plot(
                d["text_size"],
                d["comparisons"],
                marker="o",
                linewidth=1.8,
                markersize=3.5,
                label=algo,
                color=color_for(algo),
            )
        ax.set_title(f"Pattern {int(pat)}x{int(pat)}")
        ax.set_xlabel("Text Size (NxN)")
        ax.set_ylabel("Avg. Comparisons")
        if idx == 0:
            ax.legend(loc="upper left", fontsize=6.8, frameon=False)

    for ax in axes[len(pats):]:
        ax.set_visible(False)

    fig.suptitle("Comparisons vs Text Size (Random, avg over seeds)", y=1.02, fontsize=12, fontweight="bold")

    save_figure(
        fig,
        out_dir / "random_comparisons_vs_textsize.png",
        out_dir / "random_comparisons_vs_textsize_focus.png",
    )


