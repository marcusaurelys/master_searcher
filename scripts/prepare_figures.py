def plot_summary_all_sections(df_random: pd.DataFrame, df_qr: pd.DataFrame, df_contrived: pd.DataFrame, out_dir: Path) -> None:
    random_avg = df_random.groupby("algorithm")["comparisons"].mean().rename("random_avg")
    qr_avg = df_qr.groupby("algorithm")["comparisons"].mean().rename("qr_avg")
    contrived_avg = df_contrived.groupby("algorithm")["comparisons"].mean().rename("contrived_avg")
    summary = pd.concat([random_avg, qr_avg, contrived_avg], axis=1)
    summary = summary.reindex(algo_list_from_frame(summary.reset_index().rename(columns={"index": "algorithm"})))

    x = np.arange(len(summary.index))
    width = 0.24
    fig, ax = plt.subplots(figsize=(11.8, 4.4))
    series = [
        ("random_avg", "Random", "#3498db"),
        ("qr_avg", "QR", "#e74c3c"),
        ("contrived_avg", "Contrived", "#2ecc71"),
    ]
    for i, (col, label, color) in enumerate(series):
        vals = summary[col].to_numpy()
        ax.bar(
            x + (i - 1) * width,
            vals,
            width=width,
            label=label,
            color=color,
            alpha=0.90,
            edgecolor="black",
            linewidth=0.45,
        )

    ax.set_title("Average Comparisons by Section and Algorithm", fontweight="bold")
    ax.set_ylabel("Avg. Comparisons")
    ax.set_xticks(x)
    ax.set_xticklabels(summary.index, rotation=20, ha="right")
    ax.legend(ncol=3)
    save_figure(fig, out_dir / "summary_all_sections.png")


def plot_head_to_head(df_head: pd.DataFrame, out_dir: Path) -> None:
    df = _ordered_algos_for_bars(df_head.copy())
    fig, axes = plt.subplots(1, 2, figsize=(11.8, 4.2))

    bars1 = axes[0].bar(
        df["algorithm"],
        df["comparisons"],
        color=[color_for(a) for a in df["algorithm"]],
        edgecolor="black",
        linewidth=0.5,
    )
    axes[0].set_title("Equality Comparisons", fontweight="bold")
    axes[0].set_ylabel("Comparisons")
    axes[0].tick_params(axis="x", rotation=20)
    axes[0].bar_label(bars1, fmt=lambda v: f"{int(v):,}", fontsize=7, padding=2)

    bars2 = axes[1].bar(
        df["algorithm"],
        df["wall_sec"],
        color=[color_for(a) for a in df["algorithm"]],
        edgecolor="black",
        linewidth=0.5,
    )
    axes[1].set_title("Wall-Clock (s)", fontweight="bold")
    axes[1].set_ylabel("Seconds")
    axes[1].tick_params(axis="x", rotation=20)
    axes[1].bar_label(bars2, fmt=lambda v: f"{v:.2f}s", fontsize=7, padding=2)

    fig.suptitle("Head-to-Head on Maximum Input", y=1.02, fontsize=12, fontweight="bold")
    save_figure(fig, out_dir / "head_to_head.png")


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate beamer-friendly figures from CSVs")
    parser.add_argument("--source-dir", type=Path, default=Path("."))
    parser.add_argument("--out-dir", type=Path, default=Path("slides/figures"))
    args = parser.parse_args()

    configure_style()

    src_dir = args.source_dir.resolve()
    out_dir = args.out_dir.resolve()
    out_dir.mkdir(parents=True, exist_ok=True)
    cleanup_legacy_files(out_dir)

    df_random = read_csv(src_dir / "results_random.csv")
    df_qr = read_csv(src_dir / "results_qr.csv")
    df_contrived = read_csv(src_dir / "results_contrived.csv")
    df_head = read_csv(src_dir / "results_head_to_head.csv")

    plot_random_comparisons_vs_textsize(df_random, out_dir)
    plot_random_comparisons_vs_patsize(df_random, out_dir)
    plot_random_wallclock(df_random, out_dir)
    plot_qr_avg_comparisons(df_qr, out_dir)
    plot_qr_per_image(df_qr, out_dir)
    plot_qr_wallclock(df_qr, out_dir)
    plot_contrived_comparisons(df_contrived, out_dir)
    plot_contrived_wallclock(df_contrived, out_dir)
    plot_summary_all_sections(df_random, df_qr, df_contrived, out_dir)
    plot_head_to_head(df_head, out_dir)

    print(f"Generated slide figures in: {out_dir}")


if __name__ == "__main__":
    main()
